const scrollContainer = document.querySelector('.home-hero__mouse-scroll-cont');

// 2. Define the ID of the section you want to scroll to (changed to 'project')
const targetId = 'about';

// 3. Add a click event listener
scrollContainer.addEventListener('click', function() {
  // 4. Find the target element using its ID
  const targetElement = document.getElementById(targetId);

  // 5. Check if the element exists before scrolling
  if (targetElement) {
    // Use the scrollIntoView method for smooth scrolling
    targetElement.scrollIntoView({
      behavior: 'smooth' // Makes the scroll animation smooth
    });
  }
});